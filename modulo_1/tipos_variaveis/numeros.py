# Inteiro
numero_inteiro = 15
print("Inteiro = ", numero_inteiro)

# Real com ponto flutuante
numero_real = 1.12
print("Real com ponto flutuante = ", numero_real)

# type() = 
print("Tipo da variavel inteiro = ", type(numero_inteiro))
print("Tipo da variavel real = ", type(numero_real))

# soma +
soma = 3 + 1
print("3 + 1 = ", soma)

#float
print("soma em float = ", float(soma))

# subtracao -
subtracao = 3 - 1
print("3 - 1 = ", subtracao)

# multiplicacao *
multiplicacao = 2 * 3
print("2 * 3 = ", multiplicacao)

# divisao /
divisao = 5 / 2
print("5 / 2 = ", divisao)
print("tipo desta divisao = ", type(divisao))

# int()
print("divisao em inteiro = ", int(divisao))

# divisao // (numero inteiro)
divisaoInteiro = 5 // 2
print(" 5 // 2 = ", divisaoInteiro)
print("tipo desta divisao = ", type(divisaoInteiro))

# modulo % (resto da divisão)
modulo = 5 % 2
print("Modulo = ", modulo)